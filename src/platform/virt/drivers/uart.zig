const VIRT_UART_ADDRESS: usize = 0x09000000;

fn virt_uart_write_byte(value: u8) void {
    const ptr: *volatile usize = @ptrFromInt(VIRT_UART_ADDRESS);
    ptr.* = value;
}

pub fn println(comptime bytes: []const u8) void {
    for (bytes) |byte| {
        virt_uart_write_byte(byte);
    }
}
